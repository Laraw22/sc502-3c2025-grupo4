        <h2>¿Quiénes somos?</h2>
        <p>
          Somos un grupo de estudiantes universitarios comprometidos con
          impulsar el voluntariado a nivel nacional. Al analizar la realidad
          actual, identificamos un problema clave: la falta de una plataforma
          accesible y centralizada para conectar a voluntarios con
          organizaciones que necesitan apoyo.
        </p>
        <p>
          El voluntariado es una herramienta poderosa para fortalecer la
          sociedad y contribuir en áreas como la educación, el medioambiente, la
          salud y la asistencia social. Sin embargo, muchas iniciativas carecen
          de visibilidad y medios eficientes para reclutar voluntarios.
        </p>
        <p>
          Por ello, desarrollamos una solución digital que facilita la conexión
          entre organizaciones y personas interesadas en contribuir. Nuestro
          objetivo es crear un espacio donde cualquier persona pueda encontrar
          oportunidades de voluntariado de manera sencilla y participar
          activamente en causas que generen impacto positivo.
        </p>
